export function buildProgramWeeks() {
  const weeks = []
  const totalWeeks = Math.ceil(TOTAL_DAYS / 7)
  for (let week = 1; week <= totalWeeks; week += 1) {
    const range = getWeekRange(week)
    const days = []
    for (let day = range.start; day <= range.end; day += 1) {
      days.push(day)
    }
    weeks.push({
      week,
      phase: getPhaseName(getPhaseForDay(range.start)),
      range,
      days,
    })
  }
  return weeks
}

export function getCurrentStreak(completedDateKeys) {
  if (!completedDateKeys.length) return 0
  const set = new Set(completedDateKeys)
  let streak = 0
  let cursor = getLocalDateKey()

  while (set.has(cursor)) {
    streak += 1
    cursor = shiftDateKey(cursor, -1)
  }
  return streak
}

export function getLatestByDate(items) {
  if (!items.length) return null
  return [...items].sort((a, b) => a.id.localeCompare(b.id)).at(-1)
}

export function daysBetween(dateAKey, dateBKey) {
  const dateA = parseDateKey(dateAKey)
  const dateB = parseDateKey(dateBKey)
  return Math.floor((dateA.getTime() - dateB.getTime()) / 86400000)
}

export function isMeasurementDue(measurements, todayKey) {
  const latest = getLatestByDate(measurements)
  if (!latest) return true
  return daysBetween(todayKey, latest.id) >= 14
}

export function isInBodyDue(scans, todayKey) {
  const latest = getLatestByDate(scans)
  if (!latest) return true
  return daysBetween(todayKey, latest.id) >= 30
}

export function numberOrNull(value) {
  if (value === '' || value === null || typeof value === 'undefined') return null
  const parsed = Number(value)
  return Number.isNaN(parsed) ? null : parsed
}

export function formatMetric(value, suffix = '') {
  if (value === null || typeof value === 'undefined') return '—'
  return `${value}${suffix}`
}

export function difference(current, previous) {
  if (current === null || previous === null || typeof current === 'undefined' || typeof previous === 'undefined') return null
  return Number((current - previous).toFixed(1))
}

export function getImprovementLabel(current, previous, direction) {
  const delta = difference(current, previous)
  if (delta === null) return { text: 'No comparison yet', tone: 'neutral' }
  if (delta === 0) return { text: 'No change yet', tone: 'neutral' }
  const improved = direction === 'up' ? delta > 0 : delta < 0
  return {
    text: `${delta > 0 ? '+' : ''}${delta} vs last`,
    tone: improved ? 'good' : 'warning',
  }
}
